import { ApiOperation, ApiResponse } from '@nestjs/swagger';

const transactionResponseSchema = {
  type: 'object',
  properties: {
    transactions: {
      type: 'array',
      items: {
        type: 'object',
        properties: {
          transactionHash: {
            type: 'string',
            example:
              '0xfc2b3b6db38a51db3b9cb95de29b719de8deb99630626e4b4b99df056ffb7f2e',
            description: 'The hex encoded transaction hash',
          },
          transactionStatus: {
            type: 'number',
            example: 1,
            description:
              'The status of the transaction (1 for success, 0 for failure)',
          },
          blockHash: {
            type: 'string',
            example:
              '0x20c16f757d1fecd1ca00006cb5e10b541b04c70ad0ab3c4cd444f4cd9a0d437b',
            description: 'The hex encoding of the hash of the block',
          },
          blockNumber: {
            type: 'number',
            example: 4553069,
            description: 'The number of the block',
          },
          from: {
            type: 'string',
            example: '0x68ad60cc5e8f3b7cc53beab321cf0e6036962dbc',
            description: 'The ethereum address of the transaction sender',
          },
          to: {
            type: 'string',
            example: null,
            description:
              'The ethereum address of the transaction receiver or null for contract creation',
          },
          contractAddress: {
            type: 'string',
            example: null,
            description:
              'The ethereum address of the newly created contract if this is a contract creation transaction',
          },
          logsCount: {
            type: 'number',
            example: 1,
            description: 'Number of log objects generated by this transaction',
          },
          input: {
            type: 'string',
            example:
              '0x60e060405260008054600160a81b600160e81b031916650a8c0000007d60aa1b179055...',
            description:
              'The hex encoding of the data sent along with the transaction',
          },
          value: {
            type: 'string',
            example: '0',
            description: 'The value transferred in wei',
          },
        },
      },
    },
  },
};

export const getMyTransactionsOperation = ApiOperation({
  summary: 'Get my transactions',
  description: 'Returns a list of transactions saved by the authenticated user',
});

export const getMyTransactionsResponse = ApiResponse({
  status: 200,
  description: 'Successfully retrieved user transactions',
  schema: transactionResponseSchema,
});

export const getMyTransactionsResponse401 = ApiResponse({
  status: 401,
  description: 'Unauthorized - User not found',
  schema: {
    type: 'object',
    properties: {
      statusCode: { type: 'number', example: 401 },
      message: { type: 'string', example: 'User not found' },
      error: { type: 'string', example: 'Unauthorized' },
    },
  },
});

export const getMyTransactionsResponse404 = ApiResponse({
  status: 404,
  description: 'Not Found - No transactions found',
  schema: {
    type: 'object',
    properties: {
      statusCode: { type: 'number', example: 404 },
      message: { type: 'string', example: 'No transactions found' },
      error: { type: 'string', example: 'Not Found' },
    },
  },
});

export const getMyTransactionsResponse500 = ApiResponse({
  status: 500,
  description: 'Internal Server Error',
  schema: {
    type: 'object',
    properties: {
      statusCode: { type: 'number', example: 500 },
      message: { type: 'string', example: 'Internal server error' },
      error: { type: 'string', example: 'Internal Server Error' },
    },
  },
});
